export function searchDictionary(query) {
  const q = query.toLowerCase().trim();
  if (!q) return [];
  return dictionary.filter(e =>
    e.german.toLowerCase().includes(q) || e.english.toLowerCase().includes(q)
  );
}

export function getWordsByLevel(level) {
  return dictionary.filter(e => e.level === level);
}

export function getWordsByCategory(category) {
  return dictionary.filter(e => e.category === category);
}

export function getCategories() {
  return [...new Set(dictionary.map(e => e.category))];
}
